					La idea de crear la Carrera de Turismo nace a partir de la iniciativa de la Facultad de Arquitectura, cuando se establece la necesidad de generar el área de ciencias del hábitat, que debe incorporar en su interior todas las disciplinas que estudian los espacios utilizados por el hombre y las realizaciones producto de su cultura.
